// Helper functions
export function getArticle(id: string): HelpArticle | undefined {
  return HELP_ARTICLES[id];
}

export function getArticlesByCategory(category: HelpArticle["category"]): HelpArticle[] {
  return Object.values(HELP_ARTICLES).filter((article) => article.category === category);
}

export function searchArticles(query: string): HelpArticle[] {
  const lowercaseQuery = query.toLowerCase();
  return Object.values(HELP_ARTICLES).filter((article) => {
    const titleMatch = article.title.toLowerCase().includes(lowercaseQuery);
    const keywordsMatch = article.keywords?.some((keyword) =>
      keyword.toLowerCase().includes(lowercaseQuery)
    );
    return titleMatch || keywordsMatch;
  });
}
